// SECTION 6 — AUDIT TRAIL SCHEMA
// ============================================================================

export interface DisclosureAcknowledgment {
  disclosureKey: string;
  disclosureVersion: string;
  acknowledgedAt: string;        // ISO 8601 timestamp
  ipAddress?: string;
  userAgent?: string;
}

export interface AuditTrailEntry {
  assessmentId: string;
  userId?: string;
  advisorId?: string;
  consents: DisclosureAcknowledgment[];
  reportVersion: string;
  engineVersion: string;
  generatedAt: string;
  aiSanitizerInvocations: number;
  aiSanitizerViolationsBlocked: number;
  events: Array<{
    type: 'consent' | 'assessment_started' | 'assessment_completed'
        | 'report_generated' | 'pdf_downloaded' | 'email_sent'
        | 'advisor_assigned' | 'ai_output_blocked';
    at: string;
    metadata?: Record<string, unknown>;
  }>;
}

/** Constructs a fresh audit trail. Call at assessment start. */
export function newAuditTrail(args: {
  assessmentId: string;
  userId?: string;
  advisorId?: string;
}): AuditTrailEntry {
  return {
    assessmentId: args.assessmentId,
    userId: args.userId,
    advisorId: args.advisorId,
    consents: [],
    reportVersion: DISCLAIMER_VERSION,
    engineVersion: '2.0.0',
    generatedAt: new Date().toISOString(),
    aiSanitizerInvocations: 0,
    aiSanitizerViolationsBlocked: 0,
    events: [
      { type: 'assessment_started', at: new Date().toISOString() },
    ],
  };
}

/** Appends a consent acknowledgment. */
export function recordConsent(
  trail: AuditTrailEntry,
  disclosureKey: string,
  ctx?: { ipAddress?: string; userAgent?: string },
): void {
  const now = new Date().toISOString();
  trail.consents.push({
    disclosureKey,
    disclosureVersion: DISCLAIMER_VERSION,
    acknowledgedAt: now,
    ipAddress: ctx?.ipAddress,
    userAgent: ctx?.userAgent,
  });
  trail.events.push({
    type: 'consent',
    at: now,
    metadata: { disclosureKey, version: DISCLAIMER_VERSION },
  });
}

/** Records an audit trail event. */
export function recordEvent(
  trail: AuditTrailEntry,
  type: AuditTrailEntry['events'][number]['type'],
  metadata?: Record<string, unknown>,
): void {
  trail.events.push({
    type,
    at: new Date().toISOString(),
    metadata,
  });
}

/** Verifies that all required intake consents have been recorded. */
export function verifyIntakeConsentsComplete(trail: AuditTrailEntry): {
  complete: boolean;
  missing: string[];
} {
  const required = INTAKE_CONSENT_ITEMS.filter((c) => c.required).map((c) => c.key);
  const recorded = new Set(trail.consents.map((c) => c.disclosureKey));
  const missing = required.filter((k) => !recorded.has(k));
  return { complete: missing.length === 0, missing };
}

