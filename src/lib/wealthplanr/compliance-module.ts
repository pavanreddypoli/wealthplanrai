/** Quick check used by linters / pre-publish hooks. */
export function checkMarketingCopy(copy: string): {
  passed: boolean;
  violations: string[];
} {
  const lower = copy.toLowerCase();
  const violations = MARKETING_DISALLOWED_TERMS.filter((t) => lower.includes(t));
  return { passed: violations.length === 0, violations };
}

