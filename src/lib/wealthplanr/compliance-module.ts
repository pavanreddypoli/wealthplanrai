// SECTION 5 — AI OUTPUT SANITIZER
// ============================================================================

/**
 * Imperative/prescriptive language that an AI must NOT produce in any
 * client-facing or advisor-facing output.
 *
 * The sanitizer scans free-text AI outputs for these patterns and either:
 *   - rewrites them educationally, or
 *   - rejects them and triggers regeneration.
 */
/**
 * Imperative/prescriptive language patterns. Each entry is either:
 *   - a static replacement (string), or
 *   - a function that receives match groups and returns the educational rewrite.
 */
const PRESCRIPTIVE_PHRASE_PATTERNS: Array<{
  pattern: RegExp;
  educationalReplacement: string | ((match: string, ...groups: string[]) => string);
}> = [
  // Direct imperatives
  { pattern: /\byou should (buy|invest in|sell|move|transfer|switch to|put your)\b/gi,
    educationalReplacement: 'a topic to discuss with your advisor is whether to' },
  { pattern: /\b(buy|sell) (this|that|the) (stock|fund|bond|security|annuity)\b/gi,
    educationalReplacement: (_m, action: string, _det: string, prod: string) =>
      `discuss the role of this ${prod} in your portfolio with your advisor`,
  },
  { pattern: /\bmove (your|the) (ira|401\(?k\)?|portfolio|funds?) (to|into)\b/gi,
    educationalReplacement: (_m, _det: string, acct: string, _prep: string) =>
      `consider discussing whether moving your ${acct} could fit your plan`,
  },
  { pattern: /\brefinance now\b/gi,
    educationalReplacement: 'consider discussing refinancing options with a mortgage professional' },
  { pattern: /\bwe recommend\b/gi,
    educationalReplacement: 'a planning topic to discuss is' },
  { pattern: /\bour recommendation is\b/gi,
    educationalReplacement: 'a framework-aligned topic for advisor discussion is' },
  // Guarantee language
  { pattern: /\b(guaranteed|guarantee) (returns?|growth|outcomes?|wealth|savings?)\b/gi,
    educationalReplacement: (_m, _g: string, what: string) =>
      `historical patterns of ${what} (no guarantees)`,
  },
  { pattern: /\bwill (definitely|certainly|surely) (earn|gain|grow|return)\b/gi,
    educationalReplacement: (_m, _adv: string, verb: string) =>
      `may ${verb} (educational projection only)`,
  },
  // Specific product imperatives
  { pattern: /\bbuy (apple|tesla|microsoft|google|amazon|nvidia|meta)\b/gi,
    educationalReplacement: 'discuss diversified equity exposure with your advisor' },
];

export interface SanitizeResult {
  sanitized: string;
  violationsFound: number;
  changes: Array<{ original: string; replacement: string }>;
  passed: boolean;
}

/**
 * Sanitize AI-generated text. Returns the cleaned text plus an audit log of changes.
 * Use BEFORE displaying any AI output to a user.
 */
export function sanitizeAiOutput(text: string): SanitizeResult {
  let result = text;
  const changes: Array<{ original: string; replacement: string }> = [];
  let totalViolations = 0;

  for (const { pattern, educationalReplacement } of PRESCRIPTIVE_PHRASE_PATTERNS) {
    const matches = Array.from(result.matchAll(pattern));
    if (matches.length === 0) continue;

    totalViolations += matches.length;
    if (typeof educationalReplacement === 'string') {
      for (const m of matches) {
        changes.push({ original: m[0], replacement: educationalReplacement });
      }
      result = result.replace(pattern, educationalReplacement);
    } else {
      const fn = educationalReplacement;
      result = result.replace(pattern, (...args) => {
        const matchText = String(args[0]);
        const groups = args.slice(1, -2).map((g) => String(g ?? ''));
        const replacement = fn(matchText, ...groups);
        changes.push({ original: matchText, replacement });
        return replacement;
      });
    }
  }

  return {
    sanitized: result,
    violationsFound: totalViolations,
    changes,
    passed: totalViolations === 0,
  };
}

/**
 * Strict mode: returns null if any violation is found, signaling the caller
 * to regenerate rather than display a sanitized version.
 */
export function sanitizeAiOutputStrict(text: string): string | null {
  const r = sanitizeAiOutput(text);
  if (!r.passed) return null;
  return r.sanitized;
}

