/**
 * Wraps any structured output with a disclaimer envelope.
 * All engine outputs that leave the server should be wrapped this way before
 * being sent to the client.
 */
export interface DisclaimerEnvelope<T> {
  disclaimerVersion: string;
  educationalNotice: string;
  notFinancialAdvice: true;
  noAdvisorRelationship: true;
  consultProfessional: string;
  payload: T;
}

export function wrapWithDisclaimer<T>(payload: T): DisclaimerEnvelope<T> {
  return {
    disclaimerVersion: DISCLAIMER_VERSION,
    educationalNotice:
      'This output is provided for educational and informational purposes only.',
    notFinancialAdvice: true,
    noAdvisorRelationship: true,
    consultProfessional:
      'Consult a licensed CFP®, RIA, CPA, attorney, or other qualified ' +
      'professional before making financial decisions.',
    payload,
  };
}
